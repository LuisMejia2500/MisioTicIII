    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            serie.setNombre(this.jfNombreSerie.getText());
            serie.setTemporadas(Integer.parseInt(this.jfTemporadas.getText()));
            serie.setEpisodios(Integer.parseInt(this.jfEpisodios.getText()));
            serieControlador.create(serie);
            System.out.println("Se registr√≥ correctamente la serie " + jfNombreSerie.getText());            
        } catch (Exception e) {
            System.out.println(e.getMessage());
            System.out.println("Lo sentimos no se pudo registrar la serie " + jfNombreSerie.getText());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            serie = serieControlador.findSerie(Integer.parseInt(this.jfID.getText()));
            if(this.jfNombreSerie.getText().equalsIgnoreCase("")){
                serie.setNombre(serie.getNombre());                
            } else{
                serie.setNombre(this.jfNombreSerie.getText());
            }
            if(this.jfTemporadas.getText().equalsIgnoreCase("")){
                serie.setTemporadas(serie.getTemporadas());                
            }else{
                serie.setTemporadas(Integer.parseInt(this.jfTemporadas.getText()));
            }
            if(this.jfEpisodios.getText().equalsIgnoreCase("")){
                serie.setEpisodios(serie.getEpisodios());
            }else{
                serie.setEpisodios(Integer.parseInt(this.jfEpisodios.getText()));
            }            
            serie.setIdSerie(Integer.parseInt(this.jfID.getText()));
            //serie.setNombre(this.jfNombreSerie.getText());
            //serie.setTemporadas(Integer.parseInt(this.jfTemporadas.getText()));
            //serie.setEpisodios(Integer.parseInt(this.jfEpisodios.getText()));
            serieControlador.edit(serie);
            System.out.println("Se actualiz√≥ correctamente la serie " + jfNombreSerie.getText());
        } catch (Exception e) {
            System.out.println(e.getMessage());
            System.out.println("Lo sentimos no se pudo actualizar la serie " + jfNombreSerie.getText());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

